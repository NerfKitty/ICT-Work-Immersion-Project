    private void enterbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enterbtnActionPerformed
            
        if (emptyField()){
        String databaseURL = "jdbc:mysql://localhost:3306/sales and inventory management";
        String user = "root";
        String pass = "";
        String username = username_tfield.getText();
        String password = new String(password_tfield.getPassword());
        System.out.println("Attempting to Connect");
        
      
        try {
            
          try (Connection con = DriverManager.getConnection(databaseURL, user, pass)) {
              Statement s = con.createStatement();
              ResultSet rs = s.executeQuery("SELECT * FROM login WHERE username_login='"+username+"'");
              
              if(rs.next()){
                  System.out.println("DATA FOUND");
                  System.out.println("USERNAME: "+ rs.getString("username_login"));
                  System.out.println("PASSWORD: "+ rs.getString("password_login"));
                  
                  if(password.contentEquals(rs.getString("password_login"))){
                      System.out.println("Password Match");
                      MainFrame = new MainFrame();
                      MainFrame.setVisible(true);
                      
                      this.dispose();
                  }
                  else{
                      System.out.println("Wrong Password. Try again.");
                      JOptionPane.showMessageDialog(this, "Wrong Password. Try Again");
                      password_tfield.setText("");
                  }
                  
              }
              else{
                  System.out.println("DATA NOT FOUND WITH USERNAME: "+username);
                  JOptionPane.showMessageDialog(this, "Wrong Username. Try Again");
                      password_tfield.setText("");
              }
          }
          
          
          
        } catch (SQLException ex) {
            Logger.getLogger(login.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        }
    }//GEN-LAST:event_enterbtnActionPerformed

